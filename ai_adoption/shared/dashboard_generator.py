    def _build_summary_cards(self, metrics: AdoptionMetrics) -> str:
        """Build the summary cards section."""
        # Calculate adoption rate (30-day active)
        active_in_30_days = metrics.active_members_count - len(metrics.inactive_30_days)
        adoption_rate = (active_in_30_days / metrics.active_members_count * 100) if metrics.active_members_count > 0 else 0
        
        return f"""
        <div class="summary-cards">
            <div class="summary-card">
                <h3>Total Team Members</h3>
                <div class="number">{metrics.active_members_count}</div>
                <p>With active access</p>
            </div>
            <div class="summary-card">
                <h3>Active Users (30 days)</h3>
                <div class="number">{active_in_30_days}</div>
                <p>{adoption_rate:.1f}% adoption rate</p>
            </div>
            <div class="summary-card">
                <h3>Team Owners</h3>
                <div class="number">{metrics.owners_count}</div>
                <p>Admin access</p>
            </div>
            <div class="summary-card">
                <h3>Total Requests</h3>
                <div class="number">{format_number(metrics.total_requests_all_time, 1)}</div>
                <p>All time</p>
            </div>
            <div class="summary-card">
                <h3>Removed Members</h3>
                <div class="number">{metrics.removed_members_count}</div>
                <p>Access revoked</p>
            </div>
        </div>
        """
    
    def _build_team_section(self, metrics: AdoptionMetrics) -> str:
        """Build the team members section."""
        # Build owner chips
        owner_chips = "".join([
            f'<span class="user-chip owner">{m.name or m.email}</span>'
            for m in metrics.owners
        ])
        
        # Build removed member chips
        removed_chips = "".join([
            f'<span class="user-chip removed">{m.name or m.email}</span>'
            for m in metrics.removed_members
        ]) if metrics.removed_members else '<p style="color: #666;">No removed members</p>'
        
        return f"""
        <div class="section">
            <div class="section-header" style="background: linear-gradient(135deg, #4CAF50, #45a049);">
                Team Membership Overview
            </div>
            <div class="section-content">
                <div class="subsection">
                    <h4>Team Owners ({metrics.owners_count})</h4>
                    <div class="user-list">
                        {owner_chips}
                    </div>
                </div>
                <div class="subsection">
                    <h4>Removed Members ({metrics.removed_members_count})</h4>
                    <div class="user-list">
                        {removed_chips}
                    </div>
                </div>
                <div class="info-box">
                    <p><strong>Total with Access:</strong> {metrics.active_members_count} members</p>
                    <p><strong>Data Source:</strong> Cursor Admin API (/teams/members)</p>
                </div>
            </div>
        </div>
        """
    
    def _build_monthly_usage_section(self, metrics: AdoptionMetrics) -> str:
        """Build the monthly usage statistics section."""
        if not metrics.monthly_stats:
            return ""
        
        # Build table rows (chronological order)
        rows = ""
        for stats in metrics.monthly_stats:
            rows += f"""
            <tr>
                <td>{stats.month_year_str}</td>
                <td>{stats.active_users}</td>
                <td class="requests-cell">{format_number(stats.total_requests, 1)}</td>
                <td>{format_number(stats.total_interactions, 0)}</td>
            </tr>
            """
        
        return f"""
        <div class="section">
            <div class="section-header" style="background: linear-gradient(135deg, #2196F3, #1976D2);">
                Monthly Usage Statistics
            </div>
            <div class="section-content">
                <table>
                    <thead>
                        <tr>
                            <th>Month</th>
                            <th>Active Users</th>
                            <th>Total Requests</th>
                            <th>Interactions</th>
                        </tr>
                    </thead>
                    <tbody>
                        {rows}
                    </tbody>
                </table>
                <div class="info-box">
                    <p><strong>Data Source:</strong> Monthly CSV exports from Cursor Admin Dashboard</p>
                    <p><strong>Active User Definition:</strong> Made at least 1 request in the month</p>
                </div>
            </div>
        </div>
        """
    
